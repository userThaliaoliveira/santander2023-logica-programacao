//  8- Implemente um programa que encontre o maior valor em um array usando a função Math.max().
// Array de exemplo com valores numéricos
let valores = [10, 5, 8, 3, 9];

// Usa a função Math.max() para encontrar o maior valor no array
let maiorValor = Math.max(...valores);

// Exibe o maior valor encontrado
console.log("O maior valor no array é: " + maiorValor);
